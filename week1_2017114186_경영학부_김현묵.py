# 조건 : 파이썬의 변수, print 함수, string-interpolation을 모두 활용
name = "김현묵"
major = "경영학부"
grade = "3학년"
goal = "교육을 성실히 듣고, 파이썬에 대해 잘 이해한 뒤 이어지는 2학기에 들을 컴퓨터공학 관련 강의에서 조금이라도 도움되는 배경지식을 얻고 싶습니다"
print(name,major,grade,goal) # 문장이 안된다. 그렇다면 print 사이에 접속어를 넣어보자

print("경북대학교",major,grade,name,"은 이번 SW 융합교육을 통해",goal) #너무나 간단하게 되긴 했는데, 조건을 충족하지 못했다.

a = name,major,grade,goal
new_q = f'{a}'
print(new_q) # 일단 string-interpolation 대목에서 강사님이 하신대로 해봤다. 6번째 줄의 결과를 괄호 안에 넣은것 밖에 안된다.

a = '경북대학교',major,grade,name,'은 이번 SW 융합교육을 통해',goal
new_q = f'{a}'
print(new_q) # 그렇다면 a 안에 수식어와 접속어들을 넣는다면 자연스러워질까? 저 콤마와 괄호가 없어지지 않는다.

print ("안녕하세요, 경북대학교 {major} {grade} {name}은 이번 SW 융합교육을 통해\n{goal}") # 거의 다 됬다! 이스케이프 코드도 잘 먹었는데 왜...

name = "김현묵"
