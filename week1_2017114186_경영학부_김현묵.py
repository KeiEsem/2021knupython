# 강사님이 말씀하신 문자열로 만들어서 출력한다는 것이 어떤 소리인지 이해가 된다. f가 없었다면 뒤에 포맷을 설정했어야 했을 것이다.

name = "김현묵"
major = "경영학부"
grade = "3학년"
goal = "교육을 성실히 듣고, 파이썬에 대해 잘 이해한 뒤 이어지는 2학기에 들을 컴퓨터공학 관련 강의에서 조금이라도 도움되는 배경지식을 얻고 싶습니다"
print ("안녕하세요, 경북대학교 {1} {2} {0}은 이번 SW 융합교육을 통해\n{3}". format(major,grade,name,goal))
# 이런 식으로 f가 없다면 문자열이 아닌 숫자를 넣고, 이후 또 숫자가 어떤 걸 나타내는지 적어야 한다. 근데 아직 혁신이라 할 정도인지는 잘... 잘보니 문장순서도 어색하다.

name = "김현묵"
major = "경영학부"
grade = "3학년"
goal = "교육을 성실히 듣고, 파이썬에 대해 잘 이해한 뒤 이어지는 2학기에 들을 컴퓨터공학 관련 강의에서 조금이라도 도움되는 배경지식을 얻고 싶습니다"
print ("안녕하세요, 경북대학교 {1} {2} {0}은 이번 SW 융합교육을 통해\n{3}". format(name,major,grade,goal))
# f를 안쓴 버전으로 만들어 봤다. 이때 print함수 내의 순서는 내가 앞서 나열한 것이 아닌, format에서의 순서가 된다는 것도 알았다.

name = "김현묵"
major = "경영학부"
grade = "3학년"
goal = "교육을 성실히 듣고, 파이썬에 대해 잘 이해한 뒤 이어지는 2학기에 들을 컴퓨터공학 관련 강의에서 도움되는 배경지식을 얻고 싶습니다"
print (f"안녕하세요, 경북대학교 {major} {grade} {name}은 이번 SW 융합교육을 통해\n{goal}") # 최종